package com.qut.pojo;

public class Teacher {

	private int teaId;
	private String teaNo;
	private String teaPwd;
	private String teaName;
	private String teaMajor;
	private String teaMail;
	
	public int getTeaId() {
		return teaId;
	}
	public void setTeaId(int teaId) {
		this.teaId = teaId;
	}
	public String getTeaNo() {
		return teaNo;
	}
	public void setTeaNo(String teaNo) {
		this.teaNo = teaNo;
	}
	public String getTeaPwd() {
		return teaPwd;
	}
	public void setTeaPwd(String teaPwd) {
		this.teaPwd = teaPwd;
	}
	public String getTeaName() {
		return teaName;
	}
	public void setTeaName(String teaName) {
		this.teaName = teaName;
	}
	public String getTeaMajor() {
		return teaMajor;
	}
	public void setTeaMajor(String teaMajor) {
		this.teaMajor = teaMajor;
	}
	public String getTeaMail() {
		return teaMail;
	}
	public void setTeaMail(String teaMail) {
		this.teaMail = teaMail;
	}

	
	
	
}
